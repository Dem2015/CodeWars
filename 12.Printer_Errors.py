# На заводе принтер печатает этикетки для коробок. Для коробок одного типа принтеру приходится использовать цвета, которые для простоты обозначаются буквами из a to m.

# Цвета, используемые принтером, записываются в контрольную строку. Например, "хорошей" контрольной строкой будет aaabbbbhaijjjm это означает, что принтер использовал три раза цвет a, четыре раза цвет b, один раз цвет h, затем один раз цвет a...

# Иногда возникают проблемы: отсутствие цветов, техническая неисправность и создается "плохая" управляющая строка, например, aaaxbbbbyyhwawiwjjjwwm с буквами не из a to m.

# Вам нужно написать функцию printer_error которая по заданной строке вернет частоту ошибок принтера в виде строки представляющей рациональное значение, числитель которого - количество ошибок, а знаменатель - длина управляющей строки. Не сводите эту дробь к более простому выражению.

# Строка имеет длину, большую или равную единице, и содержит только буквы из aДля z.

# Примеры:
# s="aaabbbbhaijjjm"
# printer_error(s) => "0/14"

# s="aaaxbbbbyyhwawiwjjjwwm"
# printer_error(s) => "8/22"

def printer_error(s):
    errors = sum(1 for char in s if char not in 'abcdefghijklm')
    return f"{errors}/{len(s)}"

# Примеры использования:
s1 = "aaabbbbhaijjjm"
print(printer_error(s1))  # => "0/14"

s2 = "aaaxbbbbyyhwawiwjjjwwm"
print(printer_error(s2))  # => "8/22"